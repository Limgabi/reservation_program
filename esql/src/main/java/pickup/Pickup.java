package pickup;

public class Pickup {
	private int pickupNum;
	private String userID;
	private String pickupMenu;
	private String pickupTime;
	
	public int getPickupNum() {
		return pickupNum;
	}
	public void setPickupNum(int pickupNum) {
		this.pickupNum = pickupNum;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getPickupMenu() {
		return pickupMenu;
	}
	public void setPickupMenu(String pickupMenu) {
		this.pickupMenu = pickupMenu;
	}
	public String getPickupTime() {
		return pickupTime;
	}
	public void setPickupTime(String pickupTime) {
		this.pickupTime = pickupTime;
	}
	
}
